using BlazorPRF.Crypto.Abstractions.Services;
using BlazorPRF.Crypto.Testing;
using SqliteWasmBlazor.CryptoSync;

// ============================================================================
// Admin Seed Generator
//
// Runs CryptoSyncBootstrap with a hardcoded test keypair and emits a .cs file
// with HasData calls that the consumer's DbContext includes in OnModelCreating.
//
// Usage: dotnet run -- --context TestSyncContext --namespace SqliteWasmBlazor.CryptoSync.Tests --output ../SqliteWasmBlazor.CryptoSync.Tests/AdminSeed.g.cs
// ============================================================================

var contextName = "TestSyncContext";
var ns = "SqliteWasmBlazor.CryptoSync.Tests";
var outputPath = "../SqliteWasmBlazor.CryptoSync.Tests/AdminSeed.g.cs";

// Parse args
for (var i = 0; i < args.Length; i++)
{
    switch (args[i])
    {
        case "--context" when i + 1 < args.Length:
            contextName = args[++i];
            break;
        case "--namespace" when i + 1 < args.Length:
            ns = args[++i];
            break;
        case "--output" when i + 1 < args.Length:
            outputPath = args[++i];
            break;
    }
}

// Hardcoded test seed — deterministic so the generated file is stable across runs.
// In production, the admin's PRF-derived keypair would be used instead.
var crypto = new BouncyCastleCryptoProvider();
var groupEncryption = new GroupEncryptionService(crypto);
var bootstrap = new CryptoSyncBootstrap(groupEncryption);

var adminSeed = new byte[32];
for (var i = 0; i < 32; i++)
{
    adminSeed[i] = (byte)(i + 1);
}
var adminKeys = await crypto.DeriveDualKeyPairAsync(adminSeed);

var seed = await bootstrap.CreateAdminSeedAsync(
    adminKeys,
    adminUsername: "TestAdmin",
    adminEmail: "admin@test.local",
    deviceName: "Seed Device");

// Emit .cs file
var wrappedCekBase64 = Convert.ToBase64String(seed.AdminShareTarget.WrappedContentKey);

var code = $$"""
