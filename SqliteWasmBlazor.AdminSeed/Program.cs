""";

var fullPath = Path.GetFullPath(outputPath);
var dir = Path.GetDirectoryName(fullPath);
if (dir is not null && !Directory.Exists(dir))
{
    Directory.CreateDirectory(dir);
}
await File.WriteAllTextAsync(fullPath, code);
Console.WriteLine($"Admin seed written to: {fullPath}");
Console.WriteLine($"Context: {ns}.{contextName}");
Console.WriteLine($"Admin contact: {seed.AdminContact.Username} ({seed.AdminContact.Email})");
Console.WriteLine($"System group: {seed.SystemGroup.GroupContext}");
