            SharingId = "{{seed.AdminShareTarget.SharingId}}",
            IsDeleted = false
        });

        modelBuilder.Entity<DeviceSettings>().HasData(new
        {
            Id = System.Guid.Parse("{{seed.Device.Id}}"),
            ClientGuid = "{{seed.Device.ClientGuid}}",
            DeviceName = "{{seed.Device.DeviceName}}",
