            X25519PublicKey = "{{seed.AdminContact.X25519PublicKey}}",
            Ed25519PublicKey = "{{seed.AdminContact.Ed25519PublicKey}}",
            IsAdmin = true,
            IsTrusted = true,
            UpdatedAt = new System.DateTime({{seed.AdminContact.UpdatedAt.Ticks}}L, System.DateTimeKind.Utc),
            SharingScope = SharingScope.Public,
            SharingId = "{{seed.AdminContact.SharingId}}",
            IsDeleted = false
        });

        modelBuilder.Entity<ShareGroup>().HasData(new
        {
            Id = System.Guid.Parse("{{seed.SystemGroup.Id}}"),
            GroupContext = "{{seed.SystemGroup.GroupContext}}",
            KeyVersion = {{seed.SystemGroup.KeyVersion}},
            AdminPublicKey = "{{seed.SystemGroup.AdminPublicKey}}",
            CreatedAt = new System.DateTime({{seed.SystemGroup.CreatedAt.Ticks}}L, System.DateTimeKind.Utc),
            UpdatedAt = new System.DateTime({{seed.SystemGroup.UpdatedAt.Ticks}}L, System.DateTimeKind.Utc),
            SharingScope = SharingScope.Public,
            SharingId = "{{seed.SystemGroup.SharingId}}",
            IsDeleted = false
        });

        modelBuilder.Entity<ShareTarget>().HasData(new
        {
            Id = System.Guid.Parse("{{seed.AdminShareTarget.Id}}"),
            ShareGroupId = System.Guid.Parse("{{seed.AdminShareTarget.ShareGroupId}}"),
            KeyVersion = {{seed.AdminShareTarget.KeyVersion}},
            MemberPublicKey = "{{seed.AdminShareTarget.MemberPublicKey}}",
            WrappedContentKey = System.Convert.FromBase64String("{{wrappedCekBase64}}"),
            Role = SyncRole.Owner,
            GrantedByContactId = System.Guid.Parse("{{seed.AdminShareTarget.GrantedByContactId}}"),
            UpdatedAt = new System.DateTime({{seed.AdminShareTarget.UpdatedAt.Ticks}}L, System.DateTimeKind.Utc),
